/**
 * @brief Retrieves the glyph data and metrics for a specific character.
 *
 * This implementation handles the `uint16_t` storage format of the 11x14 font.
 * The font data is stored as an array of 16-bit words (native endianness),
 * where each word corresponds to a row of the character.
 *
 * @param font_data Pointer to the start of the font bitmap array.
 * @param c The character to retrieve.
 * @param w_out Pointer to store the width of the character (in pixels).
 * @param h_out Pointer to store the height of the character (in pixels).
 * @return const uint8_t* Pointer to the glyph data (cast to uint8_t* for API
 * compatibility).
 */
static const uint8_t *font_11x14_get_glyph(const uint8_t *font_data, char c,
                                           uint8_t *w_out, uint8_t *h_out) {
  uint8_t index = (uint8_t)c;
  if (index < wFONT_START_CHAR || index > 126)
    index = ' ';

  const uint8_t *glyph =
      &font_data[(index - wFONT_START_CHAR) * wFONT_GET_HEIGHT * 2];

  if (w_out) {
    // Low byte of first word contains inverted width
    // Ref: #define wFONT_GET_WIDTH(ch) (14 - (bitmap[... + 1] & 0x7))
    *w_out = 14 - (glyph[1] & 0x07);
  }

  if (h_out)
    *h_out = wFONT_GET_HEIGHT;

  return glyph;
}

const meas_font_t font_11x14 = {
    .bitmap = (const uint8_t *)x11x14_bits,
    .width_max = 11,
    .height = wFONT_GET_HEIGHT,
    .start_char = wFONT_START_CHAR,
    .end_char = 126,
    .get_glyph = font_11x14_get_glyph,
};
