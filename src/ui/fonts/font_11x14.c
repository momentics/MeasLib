/**
 * @def wFONT_START_CHAR
 * @brief The first ASCII character available in the font.
 */
#define wFONT_START_CHAR 0x16

/**
 * @def wFONT_GET_HEIGHT
 * @brief The height of each character in pixels (and rows in storage).
 */
#define wFONT_GET_HEIGHT 14

/**
 * @def wFONT_STR_HEIGHT
 * @brief The vertical stride when rendering multiple lines of text.
 */
#define wFONT_STR_HEIGHT 16

/* Macros to access font data (helpers for legacy code, updated for uint16_t) */
#define wFONT_GET_DATA(ch)                                                     \
  (&x11x14_bits[(ch - wFONT_START_CHAR) * wFONT_GET_HEIGHT])

#define wFONT_GET_WIDTH(ch)                                                    \
  (14 - (x11x14_bits[(ch - wFONT_START_CHAR) * wFONT_GET_HEIGHT] & 0x7))

/*
 * Character Width Masks
 * These macros define the width information encoded in the lower bits of the
 * first word. Usage: `0b... | CW_XX` where XX is the width. Formula: EncodedVal
 * = 14 - Width.
 */
#define CW_06 0x07 /**< Width: 7 pixels */
#define CW_07 0x06 /**< Width: 8 pixels */
#define CW_08 0x05 /**< Width: 9 pixels */
#define CW_09 0x04 /**< Width: 10 pixels */
#define CW_10 0x03 /**< Width: 11 pixels */
#define CW_11 0x02 /**< Width: 12 pixels */
#define CW_12 0x01 /**< Width: 13 pixels */
#define CW_13 0x00 /**< Width: 14 pixels */

